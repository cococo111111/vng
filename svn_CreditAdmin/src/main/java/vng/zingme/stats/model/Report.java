package vng.zingme.stats.model;

public class Report {

	private String agentID;
	private String startTime;
	private String endTime;
	private int totalTransaction;
	private int totalTransactionSuccess;
	private int totalTransactionFail;
	private double totalMoney;
	private int totalTransactionTimeOut;
	private int totalTransactionNetError;
	private int totalDiffUser;

	public String getAgentID() {
		return agentID;
	}

	public void setAgentID(String agentID) {
		this.agentID = agentID;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public int getTotalTransaction() {
		return totalTransaction;
	}

	public void setTotalTransaction(int totalTransaction) {
		this.totalTransaction = totalTransaction;
	}

	public int getTotalTransactionSuccess() {
		return totalTransactionSuccess;
	}

	public void setTotalTransactionSuccess(int totalTransactionSuccess) {
		this.totalTransactionSuccess = totalTransactionSuccess;
	}

	public int getTotalTransactionFail() {
		return totalTransactionFail;
	}

	public void setTotalTransactionFail(int totalTransactionFail) {
		this.totalTransactionFail = totalTransactionFail;
	}

	public double getTotalMoney() {
		return totalMoney;
	}

	public void setTotalMoney(double totalMoney) {
		this.totalMoney = totalMoney;
	}

	public int getTotalTransactionTimeOut() {
		return totalTransactionTimeOut;
	}

	public void setTotalTransactionTimeOut(int totalTransactionTimeOut) {
		this.totalTransactionTimeOut = totalTransactionTimeOut;
	}

	public int getTotalTransactionNetError() {
		return totalTransactionNetError;
	}

	public void setTotalTransactionNetError(int totalTransactionNetError) {
		this.totalTransactionNetError = totalTransactionNetError;
	}

	public int getTotalDiffUser() {
		return totalDiffUser;
	}

	public void setTotalDiffUser(int totalDiffUser) {
		this.totalDiffUser = totalDiffUser;
	}

	public Report() {
		super();
	}

}
