package com.services.soap.mo;

public class SpamResult {
	private int is_spam;
	private int mo_count;
	private int mo_duration;
	
	public final int getIs_spam() {
		return is_spam;
	}
	public final void setIs_spam(int is_spam) {
		this.is_spam = is_spam;
	}
	public final int getMo_count() {
		return mo_count;
	}
	public final void setMo_count(int mo_count) {
		this.mo_count = mo_count;
	}
	public final int getMo_duration() {
		return mo_duration;
	}
	public final void setMo_duration(int mo_duration) {
		this.mo_duration = mo_duration;
	}
	

}
