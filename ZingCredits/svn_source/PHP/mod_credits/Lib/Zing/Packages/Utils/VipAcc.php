	public static function createSelectTag($addCount=false)
	{
		$str = '';
		$count = 1;
		$showCount = "";
		foreach (self::$VipList as $username => $id)
		{
			if($addCount==true)
			{
				$showCount = $count." - ";
				$count++;
			}
			$str.= "<option value='$id'>$showCount$username</option>";
		}
		return $str;
	}
	
	public static function getIndexArray()
	{
		$result = array();
		foreach(self::$VipList as $key => $value)		
			$result[] = $value;
		return $result;
	}
	
	public static function getUserNameFromId($userId)
	{
		foreach(self::$VipList as $key => $value)
			if($value==$userId) return $key;
		return "";
